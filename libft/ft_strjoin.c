#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2, int k)
{
	int		i;
	int		j;
	char	*str;

	i = -1;
	j = -1;
	if (!s1 || !s2)
		return (NULL);
	if (!(str = malloc(sizeof(char) * (ft_strlen(s1) + ft_strlen(s2) + 1))))
		return (NULL);
	while (s1[++i] != '\0')
		str[i] = s1[i];
	while (s2[++j] != '\0')
		str[i + j] = s2[j];
	str[i + j] = '\0';
	if ((k == 1 || k == 3) && s1)
		free((char*)s1);
	if ((k == 2 || k == 3) && s2)
		free((char*)s2);
	return (str);
}
